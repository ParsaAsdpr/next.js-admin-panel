import { NextRequest, NextResponse as res } from "next/server";
import prisma from "../../../../../prisma/client";
import schema from "./schema";

export const GET = async (
  req: NextRequest,
  { params }: { params: { id: string } }
) => {
  const user = await prisma.users.findUnique({
    where: {
      id: params.id,
    },
    select: {
      id: true,
      name: true,
      email: true,
      role: true,
      createdAt: true,
      lastLogin: true,
      isActive: true,
    },
  });

  if (!user) return res.json({ error: "کاربر پیدا نشد" }, { status: 404 });

  return res.json({ user }, { status: 200 });
};

export const PATCH = async (
  req: NextRequest,
  { params }: { params: { id: string } }
) => {
  const user = await prisma.users.findUnique({
    where: {
      id: params.id,
    },
  });

  if (!user) return res.json({ error: "کاربر پیدا نشد" }, { status: 404 });

  const body = await req.json();
  const parsedBody = schema.safeParse(body);

  if (!parsedBody.success) return res.json(parsedBody.error, { status: 400 });

  const updatedUser = await prisma.users.update({
    where: {
      id: params.id,
    },
    data: {
      name: parsedBody.data.name,
      email: parsedBody.data.email,
      password: parsedBody.data.password,
      role: parsedBody.data.role,
      isActive: parsedBody.data.isActive,
    },
    select: {
      id: true,
      name: true,
      email: true,
      role: true,
      createdAt: true,
      lastLogin: true,
      isActive: true,
    }
  });
  return res.json({ user: updatedUser }, { status: 200 });
};

export const DELETE = async (
  req: NextRequest,
  { params }: { params: { id: string } }
) => {
  const user = await prisma.users.findUnique({
    where: {
      id: params.id,
    },
    select: {
      id: true,
      name: true,
      email: true,
      role: true,
      createdAt: true,
      lastLogin: true,
      isActive: true,
    }
  });

  if (!user) return res.json({ error: "کاربر پیدا نشد" }, { status: 404 });

  await prisma.users.delete({
    where: {
      id: params.id,
    },
  });
  return res.json(
    { message: "کاربر با موفقیت حذف شد", user: user },
    { status: 200 }
  );
};
